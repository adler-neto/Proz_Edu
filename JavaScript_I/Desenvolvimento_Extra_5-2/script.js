function operacaoBasicas (numA, numB){

    let soma = `A soma entre ${numA} + ${numB} é ${numA + numB}!`;
    console.log(soma);
    
    let subtracao = `A subtração entre ${numA} - ${numB} é ${numA - numB}!`;
    console.log(subtracao);
    
    let multiplicacao = `A multiplicação entre ${numA} x ${numB} é ${numA * numB}!`;
    console.log(multiplicacao);
    
    let divisao =  `A subtração entre ${numA} / ${numB} é ${numA / numB}!`;
    console.log(divisao);
  
  }
  
  operacaoBasicas(4, 5);